"""Dispatch an HF Job that runs `executable_coding_v1_holdout` against any
adapter_repo (or bare base model) and emits a per-problem execution score.

Why this exists:
- v6h trains with `push_adapter: false` so the adapter only persists if a later
  job is dispatched with push enabled. Once an adapter is published, this
  script is the canonical way to score it on the 10 holdout problems / 46
  hidden tests built by `build_executable_coding_v1_sft.py`.
- The job runs base+adapter on l4x1, generates one response per holdout
  problem, and scores by EXECUTION (matches the local
  `run_executable_coding_holdout.py` semantics).
- Result JSON is uploaded to a HF dataset repo (default
  `issdandavis/scbe-eval-results`) so it survives the ephemeral container.

Usage:
    python scripts/eval/dispatch_executable_holdout_inference_hf_job.py plan \
      --adapter-repo issdandavis/scbe-coding-primary-7b-qlora-v6h-executable

    python scripts/eval/dispatch_executable_holdout_inference_hf_job.py dispatch \
      --adapter-repo issdandavis/scbe-coding-primary-7b-qlora-v6h-executable

The `--adapter-repo` MUST already be published on the Hub. If you trained with
`push_adapter: false`, re-dispatch the training profile with that flipped to
`true` first.
"""

from __future__ import annotations

import argparse
import json
import os
import re
import subprocess
import sys
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

REPO_ROOT = Path(__file__).resolve().parents[2]
DEFAULT_ARTIFACT_ROOT = REPO_ROOT / "artifacts" / "hf_executable_holdout_jobs"
DEFAULT_HOLDOUT_JSONL = REPO_ROOT / "training-data" / "sft" / "executable_coding_v1_holdout.sft.jsonl"
DEFAULT_BASE_MODEL = "Qwen/Qwen2.5-Coder-7B-Instruct"
DEFAULT_RESULT_DATASET = "issdandavis/scbe-eval-results"

sys.path.insert(0, str(REPO_ROOT / "scripts" / "training_data"))
from build_executable_coding_v1_sft import PROBLEMS  # noqa: E402


def _utc_stamp() -> str:
    return datetime.now(timezone.utc).strftime("%Y%m%dT%H%M%SZ")


def _write_json(path: Path, payload: dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, indent=2, ensure_ascii=True), encoding="utf-8")


def load_holdout_prompts(jsonl_path: Path) -> list[dict[str, Any]]:
    rows = []
    for line in jsonl_path.read_text(encoding="utf-8").splitlines():
        line = line.strip()
        if not line:
            continue
        row = json.loads(line)
        msgs = row["messages"]
        rows.append({
            "problem_id": row["metadata"]["problem_id"],
            "system": next(m["content"] for m in msgs if m["role"] == "system"),
            "user": next(m["content"] for m in msgs if m["role"] == "user"),
        })
    return rows


def build_packet(
    adapter_repo: str,
    base_model: str,
    holdout_path: Path,
    artifact_root: Path,
    flavor: str,
    timeout: str,
    result_dataset: str,
    max_new_tokens: int,
    eval_id: str,
) -> dict[str, Any]:
    holdout_rows = load_holdout_prompts(holdout_path)
    if len(holdout_rows) != len(PROBLEMS):
        raise RuntimeError(
            f"Holdout row count {len(holdout_rows)} != PROBLEMS count {len(PROBLEMS)}"
        )
    run_dir = artifact_root / eval_id / _utc_stamp()
    run_dir.mkdir(parents=True, exist_ok=True)
    packet = {
        "schema_version": "scbe_executable_holdout_inference_packet_v1",
        "prepared_at_utc": _utc_stamp(),
        "eval_id": eval_id,
        "run_dir": str(run_dir),
        "script_path": str(run_dir / "run_holdout_inference_hf.py"),
        "adapter_repo": adapter_repo,
        "base_model": base_model,
        "result_dataset": result_dataset,
        "max_new_tokens": int(max_new_tokens),
        "execution": {"flavor": flavor, "timeout": timeout},
        "holdout": holdout_rows,
        "problems": PROBLEMS,
    }
    _write_json(run_dir / "inference_packet.json", packet)
    Path(packet["script_path"]).write_text(render_inference_script(packet), encoding="utf-8")
    return packet


def render_inference_script(packet: dict[str, Any]) -> str:
    packet_json = json.dumps(packet, indent=2, ensure_ascii=True)
    return f'''# /// script
# dependencies = [
#   "accelerate>=0.34.0",
#   "peft>=0.12.0",
#   "torch",
#   "transformers>=4.46.0",
#   "huggingface_hub>=0.25.0",
#   "safetensors"
# ]
# ///
"""HF Job: run executable_coding_v1_holdout against base+adapter.

Generated by scripts/eval/dispatch_executable_holdout_inference_hf_job.py.
"""
from __future__ import annotations

import json
import os
import re
import subprocess
import sys
import tempfile
from datetime import datetime, timezone
from io import BytesIO
from pathlib import Path

import torch
from huggingface_hub import HfApi, whoami
from peft import PeftModel
from transformers import AutoModelForCausalLM, AutoTokenizer

PACKET = json.loads(r"""{packet_json}""")
os.environ.setdefault("PYTHONIOENCODING", "utf-8")
os.environ.setdefault("PYTHONUTF8", "1")
os.environ.setdefault("HF_HUB_DISABLE_PROGRESS_BARS", "1")
os.environ.setdefault("TOKENIZERS_PARALLELISM", "false")

CODE_BLOCK_RE = re.compile(r"```(?:python)?\\s*\\n?(.*?)```", re.DOTALL)


def _token() -> str:
    token = os.environ.get("HF_TOKEN", "").strip() or os.environ.get("HUGGING_FACE_HUB_TOKEN", "").strip()
    if not token:
        raise RuntimeError("Missing $HF_TOKEN or $HUGGING_FACE_HUB_TOKEN")
    os.environ.setdefault("HUGGING_FACE_HUB_TOKEN", token)
    return token


def extract_code(response: str) -> str:
    if not response:
        return ""
    m = CODE_BLOCK_RE.search(response)
    if m:
        return m.group(1).strip()
    return response.strip()


def run_problem(problem: dict, code: str) -> dict:
    if not code:
        return {{
            "problem_id": problem["id"],
            "all_pass": False,
            "extracted_code_chars": 0,
            "tests": [
                {{"call": c, "expected": e, "actual": "", "pass": False, "reason": "no code extracted"}}
                for c, e in problem["tests"]
            ],
        }}
    test_results = []
    all_pass = True
    for call_str, expected_repr in problem["tests"]:
        with tempfile.TemporaryDirectory() as tmp:
            script = Path(tmp) / "candidate.py"
            script.write_text(code + f"\\n_result = {{call_str}}\\nprint(repr(_result))\\n", encoding="utf-8")
            try:
                result = subprocess.run(
                    [sys.executable, str(script)],
                    capture_output=True,
                    text=True,
                    timeout=10,
                )
            except subprocess.TimeoutExpired:
                test_results.append({{"call": call_str, "expected": expected_repr,
                                       "actual": "", "pass": False, "reason": "timeout"}})
                all_pass = False
                continue
            if result.returncode != 0:
                test_results.append({{"call": call_str, "expected": expected_repr,
                                       "actual": "", "pass": False,
                                       "reason": f"crash: {{result.stderr.strip()[-200:]}}"}})
                all_pass = False
                continue
            actual = result.stdout.strip()
            tp = actual == expected_repr
            test_results.append({{"call": call_str, "expected": expected_repr,
                                   "actual": actual, "pass": tp,
                                   "reason": "" if tp else "output mismatch"}})
            if not tp:
                all_pass = False
    return {{
        "problem_id": problem["id"],
        "all_pass": all_pass,
        "extracted_code_chars": len(code),
        "tests": test_results,
    }}


def main() -> None:
    token = _token()
    print(json.dumps({{"event": "auth", "whoami": whoami(token=token).get("name", "unknown")}}))

    base_model_id = str(PACKET["base_model"])
    adapter_repo = str(PACKET["adapter_repo"]).strip()
    use_adapter = bool(adapter_repo)

    tokenizer = AutoTokenizer.from_pretrained(base_model_id, token=token, trust_remote_code=True)
    if tokenizer.pad_token_id is None:
        tokenizer.pad_token_id = tokenizer.eos_token_id

    dtype = torch.bfloat16 if torch.cuda.is_available() and torch.cuda.is_bf16_supported() else torch.float16
    base = AutoModelForCausalLM.from_pretrained(
        base_model_id,
        token=token,
        torch_dtype=dtype if torch.cuda.is_available() else torch.float32,
        device_map="auto" if torch.cuda.is_available() else None,
        trust_remote_code=True,
    )
    if use_adapter:
        model = PeftModel.from_pretrained(base, adapter_repo, token=token)
        print(json.dumps({{"event": "adapter_loaded", "adapter_repo": adapter_repo}}))
    else:
        model = base
        print(json.dumps({{"event": "no_adapter", "base_only": True}}))
    model.eval()

    responses = {{}}
    by_problem = {{}}
    n_pass = 0
    n_total = 0
    problems_by_id = {{p["id"]: p for p in PACKET["problems"]}}
    max_new = int(PACKET["max_new_tokens"])

    for row in PACKET["holdout"]:
        pid = row["problem_id"]
        messages = [
            {{"role": "system", "content": row["system"]}},
            {{"role": "user", "content": row["user"]}},
        ]
        text = tokenizer.apply_chat_template(messages, tokenize=False, add_generation_prompt=True)
        inputs = tokenizer([text], return_tensors="pt").to(model.device)
        with torch.no_grad():
            outputs = model.generate(
                **inputs,
                max_new_tokens=max_new,
                do_sample=False,
                temperature=1.0,
                top_p=1.0,
                pad_token_id=tokenizer.pad_token_id,
            )
        gen_ids = outputs[0][inputs.input_ids.shape[1]:]
        response = tokenizer.decode(gen_ids, skip_special_tokens=True)
        responses[pid] = response
        code = extract_code(response)
        result = run_problem(problems_by_id[pid], code)
        by_problem[pid] = result
        n_total += 1
        if result["all_pass"]:
            n_pass += 1
        print(json.dumps({{"event": "problem_done", "problem_id": pid,
                           "all_pass": result["all_pass"],
                           "n_tests_pass": sum(1 for t in result["tests"] if t["pass"]),
                           "n_tests_total": len(result["tests"])}}))

    pass_rate = n_pass / n_total if n_total else 0.0
    candidate_id = adapter_repo or base_model_id
    report = {{
        "schema_version": "scbe_executable_coding_eval_v1",
        "candidate_id": candidate_id,
        "base_model": base_model_id,
        "adapter_repo": adapter_repo,
        "generated_utc": datetime.now(timezone.utc).isoformat().replace("+00:00", "Z"),
        "n_total": n_total,
        "n_pass": n_pass,
        "pass_rate": pass_rate,
        "promotion_target": 0.5,
        "promotion_passed": pass_rate >= 0.5,
        "by_problem": by_problem,
    }}
    candidate_payload = {{
        "candidate_id": candidate_id,
        "responses": responses,
    }}

    print("\\n===EXECUTABLE_HOLDOUT_REPORT===")
    print(json.dumps(report, indent=2))
    print("===EXECUTABLE_HOLDOUT_REPORT_END===\\n")

    api = HfApi(token=token)
    result_dataset = str(PACKET["result_dataset"])
    api.create_repo(result_dataset, repo_type="dataset", exist_ok=True, private=False)
    eval_id = str(PACKET["eval_id"])
    stamp = datetime.now(timezone.utc).strftime("%Y%m%dT%H%M%SZ")
    api.upload_file(
        path_or_fileobj=BytesIO(json.dumps(report, indent=2).encode("utf-8")),
        path_in_repo=f"executable_holdout/{{eval_id}}/{{stamp}}/report.json",
        repo_id=result_dataset,
        repo_type="dataset",
    )
    api.upload_file(
        path_or_fileobj=BytesIO(json.dumps(candidate_payload, indent=2).encode("utf-8")),
        path_in_repo=f"executable_holdout/{{eval_id}}/{{stamp}}/candidate_responses.json",
        repo_id=result_dataset,
        repo_type="dataset",
    )
    print(json.dumps({{"event": "report_uploaded",
                       "result_dataset": result_dataset,
                       "report_path": f"executable_holdout/{{eval_id}}/{{stamp}}/report.json"}}))


if __name__ == "__main__":
    main()
'''


def dispatch_packet(packet: dict[str, Any]) -> dict[str, Any]:
    command = [
        "hf",
        "jobs",
        "uv",
        "run",
        "--flavor",
        str(packet["execution"]["flavor"]),
        "--timeout",
        str(packet["execution"]["timeout"]),
        "--env",
        "PYTHONIOENCODING=utf-8",
        "--env",
        "PYTHONUTF8=1",
        "--env",
        "LANG=C.UTF-8",
        "--env",
        "LC_ALL=C.UTF-8",
        "--env",
        "HF_HUB_DISABLE_PROGRESS_BARS=1",
        "--env",
        "TOKENIZERS_PARALLELISM=false",
        "--secrets",
        "HF_TOKEN",
        "--detach",
        str(packet["script_path"]),
    ]
    result = subprocess.run(
        command,
        cwd=str(REPO_ROOT),
        env={**os.environ, "PYTHONIOENCODING": "utf-8", "PYTHONUTF8": "1"},
        capture_output=True,
        text=True,
        encoding="utf-8",
        errors="replace",
        check=False,
    )
    combined = result.stdout + "\n" + result.stderr
    match = re.search(r"(?:Job ID|ID|job)[^A-Za-z0-9_-]*([A-Za-z0-9_-]{8,})", combined, re.IGNORECASE)
    out = dict(packet)
    out["dispatch"] = {
        "command": command,
        "returncode": result.returncode,
        "stdout": result.stdout[-4000:],
        "stderr": result.stderr[-2000:],
        "job_id": match.group(1) if match else "",
    }
    _write_json(Path(packet["run_dir"]) / "inference_packet.json", out)
    return out


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    sub = parser.add_subparsers(dest="command", required=True)
    for name in ("plan", "dispatch"):
        item = sub.add_parser(name)
        item.add_argument("--adapter-repo", default="",
                          help="HF adapter repo. Empty = base-model-only baseline.")
        item.add_argument("--base-model", default=DEFAULT_BASE_MODEL)
        item.add_argument("--holdout", type=Path, default=DEFAULT_HOLDOUT_JSONL)
        item.add_argument("--artifact-root", type=Path, default=DEFAULT_ARTIFACT_ROOT)
        item.add_argument("--flavor", default="l4x1")
        item.add_argument("--timeout", default="30m")
        item.add_argument("--result-dataset", default=DEFAULT_RESULT_DATASET)
        item.add_argument("--max-new-tokens", type=int, default=192)
        item.add_argument("--eval-id", default="",
                          help="Per-eval label; defaults to adapter-repo basename or base-only.")
    args = parser.parse_args()
    eval_id = args.eval_id or (args.adapter_repo.split("/")[-1] if args.adapter_repo else "base-only")
    packet = build_packet(
        adapter_repo=args.adapter_repo,
        base_model=args.base_model,
        holdout_path=args.holdout,
        artifact_root=args.artifact_root,
        flavor=args.flavor,
        timeout=args.timeout,
        result_dataset=args.result_dataset,
        max_new_tokens=args.max_new_tokens,
        eval_id=eval_id,
    )
    if args.command == "dispatch":
        packet = dispatch_packet(packet)
    print(json.dumps(packet, indent=2, ensure_ascii=True))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())

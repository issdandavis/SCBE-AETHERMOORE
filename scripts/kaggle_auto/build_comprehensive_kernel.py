def dump_data() -> str:
    """Serialize the live 18-dialect registry + op tables to a base64 JSON blob."""
    dialects = {name: asdict(d) for name, d in P.REGISTRY.items()}
    data = {
        "dialects": dialects,
        "ops": {
            "BINOPS": P.BINOPS,
            "CMPS": P.CMPS,
            "FUNC2": sorted(P.FUNC2),
            "FUNC1": sorted(P.FUNC1),
        },
    }
    raw = json.dumps(data, separators=(",", ":")).encode("utf-8")
    return base64.b64encode(raw).decode("ascii")


def slice_router() -> str:
    """Lift the numpy router (TONGUES .. round_robin) verbatim, drop demo + __future__."""
    src = (ROOT / "python" / "scbe" / "geometric_router.py").read_text(encoding="utf-8")
    start = src.index("TONGUES: Tuple")
    end = src.index("# --- demo")
    body = src[start:end].rstrip()
    return (
        "import numpy as np\nfrom dataclasses import dataclass, field\n"
        "from typing import Dict, List, Sequence, Tuple\n\n" + body
    )



    for n in counts:
        r = b1_emit(n, length, seed=3)
        out.append(dict(programs=n, programs_per_s=r["programs_per_s"],
                        files_per_s=r["files_per_s"], kloc_per_s=r["kloc_per_s"]))
    return out



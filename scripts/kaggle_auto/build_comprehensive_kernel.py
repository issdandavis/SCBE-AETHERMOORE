# ---- emitter (mirrors python/scbe/polyglot.py, incl. roundabout safe mode) ----
def _sub(tmpl, **kw):
    for k, v in kw.items():
        tmpl = tmpl.replace("{" + k + "}", str(v))
    return tmpl


def _ternary(d):
    """General select(cond,t,f) derived from the dialect's own comparison template."""
    t = d["cmp_tmpl"].replace("1.0", "{t}").replace("0.0", "{f}")
    if "{t}" not in t or "{f}" not in t:
        t = "({cond} ? {t} : {f})"
    return t


def render(name, d, safe=False):
    """Return (push-expression over temp vars a/b, is_binary). Roundabouts when safe."""
    if name in BINOPS:
        op = d["binop_over"].get(name, BINOPS[name])
        expr = "a " + op + " b"
        if safe and name == "div":
            expr = _sub(_ternary(d), cond="b == 0.0", t="0.0", f="a / b")
        return expr, True
    if name in CMPS:
        return _sub(d["cmp_tmpl"], cond="a " + CMPS[name] + " b"), True
    if name in FUNC2:
        expr = _sub(d["func2"][name], a="a", b="b")
        if safe and name == "mod":
            expr = _sub(_ternary(d), cond="b == 0.0", t="0.0", f=expr)
        return expr, True
    expr = _sub(d["func1"][name], a="a")
    if safe and name == "sqrt":
        expr = _sub(_ternary(d), cond="a < 0.0", t="0.0", f=expr)
    return expr, False


def emit(names, lang, fn="tongue_fn", args=("a", "b", "c"), runnable=False, safe=False):
    d = DIALECTS[lang]
    ind = d["indent"]
    L = list(d["prelude"])
    L.append(_sub(d["fn_open"], fn=fn, params=", ".join(args)))
    L.append(ind + _sub(d["stack_init"], init=", ".join(args)))
    for n in names:
        expr, is_bin = render(n, d, safe=safe)
        pop = d["pop2"] if is_bin else d["pop1"]
        L.append(ind + pop + _sub(d["push"], expr=expr) + "  " + d["comment"] + " " + n)
    L.append(ind + d["ret"])
    L.extend(d["fn_close"])
    if runnable and d["main_tmpl"]:
        L.append("")
        L.extend(_sub(m, fn=fn) for m in d["main_tmpl"])
    return "\n".join(L) + "\n"


_BIN = sorted(set(BINOPS) | set(CMPS) | FUNC2)
_UN = sorted(FUNC1)


def rand_prog(length, rng):
    depth, ops = 3, []
    for _ in range(length):
        if depth >= 2 and rng.random() < 0.6:
            ops.append(rng.choice(_BIN))
            depth -= 1
        else:
            ops.append(rng.choice(_UN))
        if depth < 1:
            depth = 1
    return ops


def run_python(names, safe):
    ns = {}
    exec(compile(emit(names, "python", safe=safe), "p.py", "exec"), ns)  # noqa: S102
    return ns["tongue_fn"](2.0, 3.0, 4.0)



# ---- B1: emit throughput -----------------------------------------------------
def b1_emit(n, length, seed=7):
    rng = random.Random(seed)
    progs = [rand_prog(length, rng) for _ in range(n)]
    t0 = time.perf_counter()
    files = lines = chars = 0
    for p in progs:
        for lang in LANGS:
            s = emit(p, lang)
            files += 1
            lines += s.count("\n")          # output already ends in \n -> true line count
            chars += len(s)
    dt = time.perf_counter() - t0
    return dict(languages=len(LANGS), programs=n, ops=length, files=files, seconds=dt,
                programs_per_s=n / dt, files_per_s=files / dt,
                kloc_per_s=lines / 1000 / dt, mchars_per_s=chars / 1e6 / dt)


# ---- B2: roundabout parity (defined undefined-zones) -------------------------
def b2_roundabouts():
    cases = [("div0", ["eq", "div"], "ZeroDivisionError"),
             ("sqrt_neg", ["sub", "sqrt"], "ValueError")]
    out = {}
    for tag, prog, want_exc in cases:
        raw_exc = None
        try:
            run_python(prog, safe=False)
        except BaseException as e:           # pin the exact type rather than swallow all
            raw_exc = type(e).__name__
        safe_val = run_python(prog, safe=True)
        out[tag] = dict(raw_exc=raw_exc, raw_raises_expected=(raw_exc == want_exc),
                        safe_value=safe_val, safe_defined=(safe_val == 0.0))
    # safe-mode emits for every language without error, and differs from raw on the op
    emit_ok = 0
    for lang in LANGS:
        try:
            safe_src = emit(["add", "div", "sqrt", "mod"], lang, safe=True)
            raw_src = emit(["add", "div", "sqrt", "mod"], lang, safe=False)
            if safe_src and safe_src != raw_src:
                emit_ok += 1
        except Exception:
            pass
    out["safe_emit_langs"] = emit_ok
    out["total_langs"] = len(LANGS)
    return out


# ---- B3a: emitter shape self-check (NOT a compile proof) ----------------------
def b3_shape(n, length, seed=11):
    """An emitter invariant, honestly labelled: every emit is well-formed (boilerplate
    present AND delimiters balanced). This is NOT evidence the code compiles -- that is
    B3b below. Delimiter balance is the one part that could actually fail on a bad
    template, so it is a real (if weak) structural signal beyond the tautological parts."""
    rng = random.Random(seed)
    progs = [rand_prog(length, rng) for _ in range(n)]
    total = ok = balanced = 0
    for p in progs:
        for lang in LANGS:
            for safe in (False, True):
                s = emit(p, lang, runnable=True, safe=safe)
                total += 1
                d = DIALECTS[lang]
                shape_ok = s.endswith("\n") and "tongue_fn" in s and (
                    not d["fn_close"] or d["fn_close"][-1] in s)
                bal = (s.count("(") == s.count(")") and s.count("{") == s.count("}")
                       and s.count("[") == s.count("]"))
                ok += shape_ok
                balanced += bal
    return dict(programs=n, langs=len(LANGS), checks=total, shape_ok=ok,
                balanced=balanced, shape_pct=100.0 * ok / total,
                balanced_pct=100.0 * balanced / total)


# ---- B3b: REAL cross-language compile + run numeric parity --------------------

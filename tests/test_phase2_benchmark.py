"""Held-out benchmark for the injection gate — HONEST measured generalization.

The corpus (tests/data/injection_eval_corpus.json) was generated by agents that
never saw the detector: 56 paraphrased attacks that avoid literal keywords, and
32 benign security-adjacent sentences (hard negatives). So these numbers are
generalization, not dev-set fit.

This test exists to LOCK IN the measured behavior and catch regressions — NOT to
claim the detector is strong. Pattern/mood heuristics plateau here: structural
intent lifts paraphrase recall from ~11% (keyword-only) to ~25%, at ~3% benign
false positives. Robustly catching arbitrary paraphrase needs a semantic model,
not a bigger pattern list. The floor/ceiling below have margin around those
measured values. All corpus strings are INERT test data; nothing is executed.
"""

from __future__ import annotations

import importlib.machinery
import json
import types
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
_loader = importlib.machinery.SourceFileLoader("scbe_bench_mod", str(ROOT / "scbe.py"))
_mod = types.ModuleType("scbe_bench_mod")
_mod.__file__ = str(ROOT / "scbe.py")
_loader.exec_module(_mod)
score = _mod.pipeline_quick_score

CORPUS = json.loads((ROOT / "tests" / "data" / "injection_eval_corpus.json").read_text(encoding="utf-8"))


def _rates() -> tuple[float, float]:
    pos, neg = CORPUS["positives"], CORPUS["hard_negatives"]
    recall = sum(1 for p in pos if score(p)["decision"] != "ALLOW") / len(pos)
    fp = sum(1 for n in neg if score(n)["decision"] != "ALLOW") / len(neg)
    return recall, fp


def test_recall_floor_on_novel_paraphrase() -> None:
    recall, _ = _rates()
    # Measured ~0.25. Floor 0.20 catches a regression that breaks the structural
    # intent screen (keyword-only would be ~0.11 here and fail this).
    assert recall >= 0.20, f"novel-paraphrase recall regressed to {recall:.0%}"


def test_false_positive_ceiling_on_benign() -> None:
    _, fp = _rates()
    # Measured ~0.03. Ceiling 0.12 — a gate that over-blocks benign security talk
    # is broken and unusable.
    assert fp <= 0.12, f"benign false-positive rate too high: {fp:.0%}"

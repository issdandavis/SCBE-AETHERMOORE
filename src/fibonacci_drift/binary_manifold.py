# Fibonacci word reference (first 100 digits)
# Generated by: start="1", rules: 1→10, 0→1
def _fibonacci_word(n: int) -> str:
    """Generate the Fibonacci word of length n."""
    a, b = "1", "10"
    while len(b) < n:
        a, b = b, b + a
    return b[:n]


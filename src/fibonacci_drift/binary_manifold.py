def float_to_bits(value: float) -> Tuple[int, List[int], int]:
    """
    Extract the binary components of a float64.

    Returns: (sign_bit, mantissa_bits[52], exponent_int)
    """
    # Pack as IEEE 754 double
    raw = struct.pack("!d", value)
    as_int = int.from_bytes(raw, "big")

    sign = (as_int >> 63) & 1
    exponent = (as_int >> 52) & 0x7FF
    mantissa_int = as_int & ((1 << 52) - 1)

    # Extract individual mantissa bits (MSB first)
    mantissa_bits = []
    for i in range(51, -1, -1):
        mantissa_bits.append((mantissa_int >> i) & 1)

    return sign, mantissa_bits, exponent


def bits_to_walk(bits: List[int]) -> List[int]:
    """
    Convert binary digits to a random walk.
    1 → step up (+1), 0 → step down (-1).
    Returns cumulative positions.
    """
    walk = [0]
    for b in bits:
        step = 1 if b == 1 else -1
        walk.append(walk[-1] + step)
    return walk


def compute_runs(bits: List[int]) -> List[Tuple[int, int]]:
    """
    Compute run-length encoding: consecutive sequences of same bit.
    Returns list of (bit_value, run_length).
    """
    if not bits:
        return []
    runs = []
    current_bit = bits[0]
    current_len = 1
    for b in bits[1:]:
        if b == current_bit:
            current_len += 1
        else:
            runs.append((current_bit, current_len))
            current_bit = b
            current_len = 1
    runs.append((current_bit, current_len))
    return runs


def fibonacci_similarity(run_lengths: List[int]) -> float:
    """
    Measure how well a sequence of run lengths matches Fibonacci structure.

    The Fibonacci word has run lengths that are predominantly 1 and 2,
    with occasional 3s, following the Fibonacci ratio.

    Returns 0.0 (no similarity) to 1.0 (perfect Fibonacci structure).
    """
    if not run_lengths:
        return 0.0

    # Fibonacci word run lengths are dominated by 1s and 2s
    fib_runs = [1, 2]  # The two most common run lengths in Fibonacci word
    count_fib = sum(1 for r in run_lengths if r in fib_runs)
    fib_ratio = count_fib / len(run_lengths)

    # Also check: ratio of 1s to 2s should approach 1/phi
    ones = sum(1 for r in run_lengths if r == 1)
    twos = sum(1 for r in run_lengths if r == 2)
    if twos > 0:
        ratio = ones / twos
        ratio_score = 1.0 - min(abs(ratio - PHI) / PHI, 1.0)
    else:
        ratio_score = 0.5

    # Long runs (>3) break Fibonacci structure
    long_runs = sum(1 for r in run_lengths if r > 3)
    long_penalty = long_runs / max(len(run_lengths), 1)

    return max(0.0, fib_ratio * 0.5 + ratio_score * 0.3 - long_penalty * 0.2)



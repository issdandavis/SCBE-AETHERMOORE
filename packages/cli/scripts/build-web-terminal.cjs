#!/usr/bin/env node
'use strict';

/**
 * build-web-terminal.cjs
 *
 * Wraps the REAL CLI rendering modules (lib/ui.js + lib/terminal-frontend.js)
 * into a single browser bundle so the website can run the exact same code the
 * terminal does — no hand-ported fork. The only browser shim is a tiny CommonJS
 * loader + a `process` stub; the module source is copied verbatim, so the web
 * demo cannot silently drift from the CLI. Re-run after changing either module:
 *
 *   node packages/cli/scripts/build-web-terminal.cjs
 *
 * Output: docs/static/scbe-terminal-web.js  (exposes window.SCBETerm)
 */

const fs = require('fs');
const path = require('path');

const CLI_LIB = path.resolve(__dirname, '..', 'lib');
const OUT = path.resolve(__dirname, '..', '..', '..', 'docs', 'static', 'scbe-terminal-web.js');

function read(name) {
  return fs.readFileSync(path.join(CLI_LIB, name), 'utf8');
}

const uiSrc = read('ui.js');
const tfSrc = read('terminal-frontend.js');

// Indent a module body so it reads cleanly inside the factory function.
function indent(src) {
  return src
    .split('\n')
    .map((line) => (line.length ? '      ' + line : line))
    .join('\n');
}

const banner = `// AUTO-GENERATED by packages/cli/scripts/build-web-terminal.cjs — do not edit by hand.
// Source of truth: packages/cli/lib/ui.js + packages/cli/lib/terminal-frontend.js
// Re-run the build script after changing either module.`;

const bundle = `${banner}
(function (root) {
  'use strict';

  // Minimal browser shims. The CLI modules only reach for process.* through
  // explicit fallbacks; when the page passes a full context + ui opts these are
  // never hit, but we stub them so the bundle is self-sufficient regardless.
  var process = {
    platform: 'web',
    version: 'v20.0.0',
    cwd: function () { return '/repo'; },
    env: {},
    stdout: { isTTY: false, columns: 88 },
  };

  var __mods = {};
  function __def(name, factory) {
    var module = { exports: {} };
    factory(module, module.exports, __req);
    __mods[name] = module.exports;
  }
  function __req(name) {
    if (Object.prototype.hasOwnProperty.call(__mods, name)) return __mods[name];
    throw new Error('web-terminal bundle: unknown module ' + name);
  }

  __def('./ui', function (module, exports, require) {
${indent(uiSrc)}
  });

  __def('./terminal-frontend', function (module, exports, require) {
${indent(tfSrc)}
  });

  var tf = __req('./terminal-frontend');
  var uimod = __req('./ui');

  // ANSI (SGR) -> HTML. Mirrors the SGR codes ui.js actually emits.
  var ANSI_COLOR = {
    31: '#ff8080', 32: '#5bd68b', 33: '#e6c14b', 34: '#8cb4ff',
    35: '#c792ea', 36: '#56c7d6', 37: '#e8e8e8', 90: '#8a93a2',
    91: '#ff8080', 92: '#86efac', 93: '#f0bf67',
  };
  function escapeHtml(s) {
    return s.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
  }
  function ansiToHtml(input) {
    var re = /\\x1b\\[([0-9;]*)m/g;
    var fg = null, bold = false, dim = false, ital = false, under = false;
    var res = '', idx = 0, m;
    function span(text) {
      if (!text) return '';
      var st = [];
      if (fg) st.push('color:' + fg);
      if (bold) st.push('font-weight:700');
      if (dim) st.push('opacity:.6');
      if (ital) st.push('font-style:italic');
      if (under) st.push('text-decoration:underline');
      var e = escapeHtml(text);
      return st.length ? '<span style="' + st.join(';') + '">' + e + '</span>' : e;
    }
    while ((m = re.exec(input))) {
      res += span(input.slice(idx, m.index));
      idx = re.lastIndex;
      var codes = m[1] === '' ? [0] : m[1].split(';').map(Number);
      for (var i = 0; i < codes.length; i += 1) {
        var c = codes[i];
        if (c === 0) { fg = null; bold = false; dim = false; ital = false; under = false; }
        else if (c === 1) bold = true;
        else if (c === 2) dim = true;
        else if (c === 3) ital = true;
        else if (c === 4) under = true;
        else if (c === 22) { bold = false; dim = false; }
        else if (c === 23) ital = false;
        else if (c === 24) under = false;
        else if (ANSI_COLOR[c] !== undefined) fg = ANSI_COLOR[c];
      }
    }
    res += span(input.slice(idx));
    return res;
  }

  root.SCBETerm = {
    buildTerminalFrontendPayload: tf.buildTerminalFrontendPayload,
    renderTerminalFrontend: tf.renderTerminalFrontend,
    normalizeReceipt: tf.normalizeReceipt,
    ui: uimod.ui,
    ansiToHtml: ansiToHtml,
  };
})(typeof window !== 'undefined' ? window : this);
`;

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, bundle, 'utf8');
process.stdout.write(
  'wrote ' +
    path.relative(path.resolve(__dirname, '..', '..', '..'), OUT) +
    ' (' +
    bundle.length +
    ' bytes)\n'
);

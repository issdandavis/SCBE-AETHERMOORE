def _pdf_page_stream(text_lines: list[str], page_width: float = 612, page_height: float = 792) -> bytes:
    """Build a single-page PDF content stream for Courier text."""
    font_size = 9
    leading = 11
    margin_left = 36
    margin_top = page_height - 36

    stream_lines = [f"BT /F1 {font_size} Tf"]
    y = margin_top
    for line in text_lines:
        if y < 36:
            break  # Simple: stop if we run out of page
        escaped = line.replace("\\", "\\\\").replace("(", "\\(").replace(")", "\\)")
        stream_lines.append(f"{margin_left} {y:.1f} Td ({escaped}) Tj")
        y -= leading
        stream_lines.append(f"0 -{leading} Td")

    # Reset: we used relative Td after the first absolute one — fix with absolute positioning
    # Actually let's redo with absolute positioning per line
    stream_lines = [f"BT /F1 {font_size} Tf"]
    y = margin_top
    for line in text_lines:
        if y < 36:
            break
        escaped = line.replace("\\", "\\\\").replace("(", "\\(").replace(")", "\\)")
        stream_lines.append(f"1 0 0 1 {margin_left} {y:.1f} Tm ({escaped}) Tj")
        y -= leading

    stream_lines.append("ET")
    return "\n".join(stream_lines).encode("latin-1", errors="replace")


def generate_pdf(inp: TaxInput, result: TaxResult, output_path: str | Path) -> Path:
    """Generate a minimal printable PDF with the tax summary."""
    text = generate_text_summary(inp, result)
    all_lines = text.split("\n")

    # Split into pages (max ~65 lines per page at 9pt/11pt leading on letter)
    max_lines_per_page = 65
    pages: list[list[str]] = []
    for i in range(0, len(all_lines), max_lines_per_page):
        pages.append(all_lines[i : i + max_lines_per_page])

    # Build PDF manually
    objects: list[bytes] = []
    offsets: list[int] = []
    pdf = bytearray()

    def add_obj(content: bytes) -> int:
        obj_num = len(objects) + 1
        offsets.append(len(pdf))
        obj_bytes = f"{obj_num} 0 obj\n".encode() + content + b"\nendobj\n"
        pdf.extend(obj_bytes)
        objects.append(obj_bytes)
        return obj_num

    # Header
    pdf.extend(b"%PDF-1.4\n%\xe2\xe3\xcf\xd3\n")

    # Object 1: Catalog
    catalog_num = add_obj(b"<< /Type /Catalog /Pages 2 0 R >>")

    # Object 2: Pages (placeholder — we'll fix the Kids array)
    pages_placeholder_offset = len(pdf)
    pages_obj_index = len(objects)
    add_obj(b"<< /Type /Pages /Kids [] /Count 0 >>")  # Placeholder

    # Object 3: Font
    font_num = add_obj(b"<< /Type /Font /Subtype /Type1 /BaseFont /Courier >>")

    # Build each page
    page_obj_nums = []
    for page_lines in pages:
        stream_data = _pdf_page_stream(page_lines)
        stream_num = add_obj(
            b"<< /Length " + str(len(stream_data)).encode() + b" >>\nstream\n" + stream_data + b"\nendstream"
        )
        page_num = add_obj(
            f"<< /Type /Page /Parent 2 0 R /MediaBox [0 0 612 792] "
            f"/Contents {stream_num} 0 R "
            f"/Resources << /Font << /F1 {font_num} 0 R >> >> >>".encode()
        )
        page_obj_nums.append(page_num)

    # Now rewrite Pages object with actual kids
    kids_str = " ".join(f"{n} 0 R" for n in page_obj_nums)
    new_pages_obj = f"<< /Type /Pages /Kids [{kids_str}] /Count {len(page_obj_nums)} >>".encode()

    # We need to rebuild the PDF with the corrected Pages object
    # Simpler approach: rebuild from scratch
    pdf2 = bytearray()
    offsets2: list[int] = []
    obj_contents: list[bytes] = []

    pdf2.extend(b"%PDF-1.4\n%\xe2\xe3\xcf\xd3\n")

    def add_obj2(content: bytes) -> int:
        num = len(obj_contents) + 1
        offsets2.append(len(pdf2))
        obj_bytes = f"{num} 0 obj\n".encode() + content + b"\nendobj\n"
        pdf2.extend(obj_bytes)
        obj_contents.append(content)
        return num

    # 1: Catalog
    add_obj2(b"<< /Type /Catalog /Pages 2 0 R >>")

    # 2: Pages (with correct kids — we know page nums will be 4,6,8... for stream+page pairs starting at obj 4)
    # Font is obj 3, then pairs of (stream, page) for each page
    page_nums = [3 + 1 + i * 2 + 1 for i in range(len(pages))]  # 5, 7, 9, ...
    kids_str = " ".join(f"{n} 0 R" for n in page_nums)
    add_obj2(f"<< /Type /Pages /Kids [{kids_str}] /Count {len(pages)} >>".encode())

    # 3: Font
    font_ref = add_obj2(b"<< /Type /Font /Subtype /Type1 /BaseFont /Courier >>")

    # Pages
    for page_lines in pages:
        stream_data = _pdf_page_stream(page_lines)
        stream_ref = add_obj2(
            b"<< /Length " + str(len(stream_data)).encode() + b" >>\nstream\n" + stream_data + b"\nendstream"
        )
        add_obj2(
            f"<< /Type /Page /Parent 2 0 R /MediaBox [0 0 612 792] "
            f"/Contents {stream_ref} 0 R "
            f"/Resources << /Font << /F1 {font_ref} 0 R >> >> >>".encode()
        )

    # Cross-reference table
    xref_offset = len(pdf2)
    pdf2.extend(b"xref\n")
    pdf2.extend(f"0 {len(obj_contents) + 1}\n".encode())
    pdf2.extend(b"0000000000 65535 f \n")
    for off in offsets2:
        pdf2.extend(f"{off:010d} 00000 n \n".encode())

    # Trailer
    pdf2.extend(f"trailer\n<< /Size {len(obj_contents) + 1} /Root 1 0 R >>\n".encode())
    pdf2.extend(f"startxref\n{xref_offset}\n%%EOF\n".encode())

    out = Path(output_path)
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_bytes(bytes(pdf2))
    return out
